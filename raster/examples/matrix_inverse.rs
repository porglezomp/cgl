extern crate cgl_math;

use cgl_math::{Mat2, Mat4};

fn main() {
    let mat = Mat4::new([[8.0, 4.0, 7.0, 2.0],
                         [13.0, 14.0, 9.0, 11.0],
                         [15.0, 6.0, 10.0, 12.0],
                         [16.0, 17.0, 18.0, 19.0]]);
    println!("{:?}", mat.inverted());
    println!("{:?}", Mat2::identity().inverted());
    // 0.0449 0.0764 0.088 -0.1046
    // 0.0067 0.1215 -0.1014 -0.007
    // 0.1375 -0.1472 -0.065 0.1118
    // -0.174 -0.0337 0.0782 0.0411
}
